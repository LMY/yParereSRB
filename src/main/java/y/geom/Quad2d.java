package y.geom;

public class Quad2d {
	public double refvalue;

	public double xmin;
	public double ymin;
	public double xmax;
	public double ymax;
	
	public Quad2d(double refvalue, double xmin, double ymin, double xmax, double ymax) {
		this.refvalue = refvalue;
		this.xmin = xmin;
		this.ymin = ymin;
		this.xmax = xmax;
		this.ymax = ymax;
	}
}
